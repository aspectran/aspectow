-- User accounts
CREATE TABLE asc_user (
    user_id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR2(50) NOT NULL UNIQUE,
    password VARCHAR2(100) NOT NULL,
    nickname VARCHAR2(50),
    email VARCHAR2(100),
    status VARCHAR2(10) DEFAULT 'NORMAL' NOT NULL,
    last_login_at TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    PRIMARY KEY (user_id)
);

COMMENT ON TABLE asc_user IS 'User accounts';

-- Roles
CREATE TABLE asc_role (
    role_id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
    role_name VARCHAR2(50) NOT NULL UNIQUE,
    description VARCHAR2(200),
    PRIMARY KEY (role_id)
);

COMMENT ON TABLE asc_role IS 'Roles';

-- User-Role mapping
CREATE TABLE asc_user_role (
    user_id NUMBER(19) NOT NULL,
    role_id NUMBER(19) NOT NULL,
    PRIMARY KEY (user_id, role_id),
    FOREIGN KEY (user_id) REFERENCES asc_user(user_id) ON DELETE CASCADE,
    FOREIGN KEY (role_id) REFERENCES asc_role(role_id) ON DELETE CASCADE
);

COMMENT ON TABLE asc_user_role IS 'User-Role mapping';

-- Permissions
CREATE TABLE asc_permission (
    perm_id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
    perm_code VARCHAR2(50) NOT NULL UNIQUE,
    description VARCHAR2(200),
    PRIMARY KEY (perm_id)
);

COMMENT ON TABLE asc_permission IS 'Permissions';

-- Role-Permission mapping
CREATE TABLE asc_role_permission (
    role_id NUMBER(19) NOT NULL,
    perm_id NUMBER(19) NOT NULL,
    PRIMARY KEY (role_id, perm_id),
    FOREIGN KEY (role_id) REFERENCES asc_role(role_id) ON DELETE CASCADE,
    FOREIGN KEY (perm_id) REFERENCES asc_permission(perm_id) ON DELETE CASCADE
);

COMMENT ON TABLE asc_role_permission IS 'Role-Permission mapping';

-- Login History
CREATE TABLE asc_login_history (
    history_id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR2(50) NOT NULL,
    login_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ip_address VARCHAR2(45),
    user_agent VARCHAR2(500),
    success_yn CHAR(1) DEFAULT 'Y' NOT NULL,
    PRIMARY KEY (history_id)
);

COMMENT ON TABLE asc_login_history IS 'Login History';

-- Vault (Encrypted Tokens)
CREATE TABLE asc_vault (
    vault_id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
    label VARCHAR2(100) NOT NULL,
    token_type VARCHAR2(20) DEFAULT 'SIMPLE' NOT NULL,
    encrypted_value VARCHAR2(500) NOT NULL,
    description VARCHAR2(500),
    valid_until TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    PRIMARY KEY (vault_id)
);

COMMENT ON TABLE asc_vault IS 'Vault (Encrypted Tokens)';

